					<blockquote style="margin:10px 10px 0 0;padding-top: 4px;">
						<i>
							В боевых искусствах  существуют  три  основных  аспекта. <br />
							<span style="text-decoration: underline;">Во-первых,  умение  видеть  и  познавать  самого  себя.</span> <br />
							<span style="text-decoration: underline;">Во-вторых,  решимость,  через  преодоление  глупости,  слабости  и  всего  ненужного.</span><br />
							<span style="text-decoration: underline;">И, наконец,  искренность,  чувствование,  преданность,  глубина  души  и  понимание  сердца.</span>
