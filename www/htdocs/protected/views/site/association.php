			Согласно трактату «Цзисяо синьшу» («Новая книга записей о Достижениях [в военном деле]»), который написал знаменитый генерал времени династии Мин  Ци Цзигуан, в то время прославились три школы боя с копьем — школы семей Ян, Ма и Ша.</p>
		
