		<p>В последнее время, в современном мире практически во всех боевых искусствах,  всё чётче стала проявляться тенденция отхода от спортивных направлений и возврата к истокам воинского мастерства.
			На фоне обильной информации по ушу, как из за возможности выезжать на соревнования и учёбу, так и из за обилия учебного материала стало ясно, что ушу имеет два направления – спортивное ушу и традиционное, т.е народное, что имеет более известное название – кунгфу (мастерство).  У этих направлений две разные цели. Спортивное ушу ориентировано на молодежь и демонстрацию спортивной техники – акробатики, ловкости, скорости с элементами ушу. Для этого специально созданы спортивные соревноательные комплексы, которые представляют собой нарезку из основных стилей ушу положенных на довольно таки сложную акробатику. 
			Традиционное ушу, во главу угла ставит воспитание морали и Духа, достижение здоровья и долголетия, воспитание целостной личности и постановку индивидуальной техники боя. Традиционные боевые искусства очень глубоко изучают философию, строение человека и окружающего его мира, их взаимосвязи, законы мироздания, по которым мы живём. Всё это позволяет нам жить в гармонии с собой и с окружающим Миром. Традиционное ушу открыто всем- мужчинам и женщинам, не зависимо от возраста. Это позволяет  совершенствоваться, как в физическом, так и в духовном плане,  и человек может  посвятить этому всю свою жизнь.  Это те вопросы, которые волнуют не только отдельного человека, но эти, же вопросы являются важными и для государственной политики относительно духовного и физического здоровья своего народа. 
			Поэтому, в  декабре 2011года, при поддержке Центра Развития Единоборств Республики Узбекистан, было принято решение о создании на базе спортивного Клуба «Shen Lung Dao» Ассоциации Традиционного ушу Республики Узбекистан. Председателем Ассоциации традиционного ушу Республики Узбекистан был назначен Борзов Владимир Георгиевич.
