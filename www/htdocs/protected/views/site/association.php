		<p>В октябре 2010, Минаков А и Гукасян В представляют Узбекистан на IV Чемпионате Мира по традиционному Ушу, проходившему в Китае, в городе Шиян. На участие в Чемпионате   были заявлены 2500  участников  из  84 стран, и  Минаков Артём завоёвывает серебряную медаль в программе Нань цюань, а Гукасян Владислав  занимает  пятнадцатое место, в программе тай цзи цюань.  
