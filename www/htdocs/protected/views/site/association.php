		<p>Испокон веков древняя земля Узбекистана славилась своими палванами – мужественными, храбрыми, сильными духом и волей богатырями. И эта воинская традиция многие столетия передавалась из поколения в поколение. Во все времена единоборства были популярны среди нашего народа. Неудивительно, что и в наши дни боевые искусства пользуются широкой популярностью среди молодёжи. Ведь боевое искусство не просто делает людей физически здоровыми, и укрепляют их духовность, но и прививает высокие нравственные качества. Процессы физического воспитания подрастающего поколения, создание в стране всех необходимых условий для занятий спортом способствовали привлечению всё большего числа подростков в спортивные секции и, в частности, в которых изучают секреты восточных единоборств. Традиционное Ушу, пожалуй, самый древний вид боевых искусств. Если проследить во времени проникновение и развитие традиционных видов боевых искусств и ушу, в том числе и в Центральной Азии, то можно выделить несколько путей.</p>


