		<p>Школы боя с шестом, копьем и мечом. Шест является одним из самых популярных видов оружия у мусульман, что нашло отражение в широко известной поговорке «на юге кулаки — на севере ногй, на востоке копья — на западе шесты». В средние века мусульмане проживали в основном на северо-западе Китая, там были популярны такие школы боя на шестах как «одноглавый шест матери и сына» и «двуглавый шест-полоса». В книге Ци Цзигуана упомянуты такие мусульманские школы боя на копьях, как «жердь семьи Ша» и «копье семьи Ма.</p>


		<p>Как видно из выше приведённых исторических справок, современный интерес к традиционному ушу имеет вполне реальную основу для изучения этой традиции.</p>


		<p>Интерес к ушу в нашей Республике начал просыпаться в 70-Х годах. Создавались стихийные группы, в которых  изучали различные направления ушу, основываясь на обрывочных знаниях из книг Р. Хаберзецера, перепечаток из китайских журналов по ушу и фильмах Брюса Ли. Во всём многообразии ушу, на такой теоретической базе, было   просто невозможно  разобраться. 
			Занимались ушу  как могли.  Кто - то пытался,  как то систематизировать обрывки знаний. Некоторые, создавали гибриды, синтезировав методы  увиденного в кино ушу  и  каратэ, некоторые пошли дальше, придумав себе мистических учителей и фактически, занялись изобретательством велосипеда.   
			Тай цзи цюань воспринимался как гимнастика, спортивное направление ушу пропагандировалось как боевое  искусство. Все рвались в бой, и практиковали саньда,  основываясь на методах каратэ и рукопашного боя. 
			«самодельное» ушу пышно расцветало. Вопрос о систематизации ушу, как  спортивного вида единоборств, ещё не стоял - катастрофически не хватало информации, а тем более квалифицированных преподавателей.
			В 1989 году была официально зарегистрирована первая Школа традиционного ушу «Парящий дракон» под руководством Борзова Владимира Георгиевича. Основным направлением развиваемом в  Школе было саньда, цинна, шуайцзяо, а так же традиционные стили ушу – нань цюань, цзуй цюань, хуань цюань. Особое внимание уделялось работе с оружием – технике меча дао и шеста. С 1995 года Школа  входит в Федерацию рукопашного боя МВД РУз и выступает за спортивное общество «ДИНАМО». В этом же году, ученики Школы, на Кубке Республики в программах  саньда и таолу, завоевывают кубок Чемпионата. В 2001 году   на VI Чемпионате Мира по спортивному ушу, была впервые заявлена сборная Республики Узбекистан. Ученица Школы, Качурина Лиля  становиться серебряным призёром Чемпионата Мира в программе с мечом-цзянь  и поднимает флаг нашей страны.
			В 2003 году,  ученики Школы, представляя Узбекистан, участвуя в открытом Чемпионате Казахстана - Минаков А, Султанов А, Хегай С, Садыкходжаев К и Московская Р. – завоёвывают семь золотых медалей.</p>

