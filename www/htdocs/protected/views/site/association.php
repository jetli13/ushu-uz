		<p><b>
				Тунбэй пигуацюань.
			</b>
			Этот стиль постепенно сложился из нескольких самостоятельных стилей. Изначальным был «пигуацюань» («кулак рубки и подвешивания»), он упоминается еще в трактате Ци Цзигуана «Цзисяо синьшу». «Рубка» — это рубящее движение выпрямленной рукой сверху вниз, «подвешивание» — движение прямой рукой снизу вверх. Стиль предназначен для ведения боя преимущественно на дальней дистанции, его техника основана на рубящих движениях прямыми руками в разных плоскостях.
			Некоторые люди сочетали в своих тренировках бацзицюань и пигуацюань. Знаменитый мастер Ли Шувэнь говорил, что «если соединишь бацзи и пигуа — то будут бояться и святые и нечисть». Именно в таком виде — как соединение пигуа и бацзи - изучали тунбэйцюань великие мастера, братья Ма Фэнту, мусульмане по национальности. Благодаря их деятельности в начале XX века тунбэйюцань распространился по всему северу Китая, вобрав в себя по пути еще два стиля — «фаньцзыцюань» («кулак вращений») и «чуоцзяо» («втыкающиеся стопы»).
			Основой пигуацюань служил комплекс, который так и назывался «пигуа». Также тренировали комплекс «цинлунцюань» («кулак темного дракона»). Ма Фэнту разработал еще два комплекса — «фэйхуцюань» («кулак летающего тигра»), преподавав в 1930-х годах в китайской армии для обучения рукопашному бою, и «тайшуцюань» («высокоизящный кулак»). Из оружия в стиле используется весь классический набор (шест, копье, меч-дао, меч-цзянь), а также парные мечи-дао, фениксоголовый клевец, кол преграждения врат, трехсекционный цеп и т.п.</p>

