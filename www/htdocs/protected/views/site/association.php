			Самой знаменитой охранной службой Китая в это время была "Хуйю бяоцзюй" или "Работающие вместе", которая принадлежала старшему евнуху императрицы Цыси. Охранные агентства в Китае определялись терминами: "ху юань" - что означает "защита места" или "цзоу бяо" - "ходячая охрана". Первые охраняли резиденции и жилища, вторые - сопровождали караваны и людей во время путешествий. Поначалу охранные службы были малоорганизованы и предлагали свои услуги всем, кто нуждался в защите и способен был ее оплатить.
			Многие мастера боевых искусств основной заработок получали тем, что нанимались охранять караваны или шли в личные телохранители. Человек, который работал охранником, обязан был быть хорошим наездником, отлично управляться с самым разным оружием и владеть хорошей техникой "ближнего боя". Оружие делилось на открытое  и тайное. Открытое носилось, соответственно, открыто, демонстрируя окружающим, что конвой охраняют вооруженные люди. Тайное оружие пряталось под одеждой на случай необходимости. Это могли быть различные цепи, кинжалы и прочее.
			Своеобразным документом, косвенно подтверждающим проникновение боевых систем по шелковому пути, может являться роман  Ученьэня « Приключения Танского монаха или Путешествие на Запад»  
			У Ченьэнь( У Чэиьэнь, 1500-1582гг.), известный литератор эпохи Мин, в 1553 году создал свой роман «Путешествие на Запад». Эта книга представляет собой рассказ о реальном путешествии  буддийского патриарха,  одного  из основоположников китайского буддизма Сюань Цзана, на Запад в Индию, за священными книгами (сутрами). Это опасное (тем более в 629 г.) и интереснейшее путешествие продлилось 17 лет. Вскоре после своего возвращения Сюань Цзан, по совету императора надиктовал своему ученику, монаху Бянь-цзану путевые заметки- отчёт обо всём, что  он видел в пути сам или слышал от других. На протяжении веков эта история, благодаря фантазии китайского народа и Ученьэня превратилась в  эпитческую легенду о Сюань Цзяне и его  спутниках, насчитывающюю более сотни глав. Автор даёт в сопровождение  Сюань-Цзяну трёх фантастических спутника: Кабана Чжу Бадзе, полубеса Шасэна и Сун Укуна – царя обезьян.</p>

