			<b>
				Во первых, создание Великого Шелкового пути.
			</b>

			Старая Северная дорога возникла по инициативе императора У-ди. В 121 году до н. э. первый верблюжий караван с шёлком и бронзовыми зеркалами направился к Ферганскому оазису через Турфанскую впадину вдоль Огненных гор и отрогов Тянь-Шаня. Но развернувшаяся торговля была прервана разорительными войнами  в той местности в 17—27 годах н. э. Однако торговля вскоре продолжилась, но уже по новому пути — Южной дороге.                                                                Великий шёлковый путь сыграл большую роль в развитии экономических и культурных связей народов Передней Азии, Кавказа, Средней Азии и Китая.  Например,  он служил проводником в  проникновении  различных инноваций, в том числе в искусстве (танцы, музыка, изобразительное искусство, архитектура, боевые исскуства), религии (христианство, буддизм, ислам, манихейство), различных технологий (само производство шёлка, а также пороха, бумаги и т. п.)
