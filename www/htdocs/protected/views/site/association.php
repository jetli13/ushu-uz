		<p>Популярный среди мусульман стиль «хуэйхуэй шиба чжоу» - («18 мусульманских локтей») считался «квинтэссенцией боевой техники». Этот стиль считался типично мусульманским, ему никогда не обучали представителей других национальностей. Как легко догадаться по названию, в основном там используются атакующие и защитные действия локтями.
			Стиль всегда был довольно закрытым, и в наше время считался безвозвратно утеряным, однако в конце 1970-х годов группа по исследованию народного ушу неожиданно обнаружила живого носителя стиля. Это был мастер Цзюй Куй, 1896 года рождения. Он с шести лет занимался ушу, с 24 лет зарабатывал на жизнь преподаванием боевого искусства, а в 33 года для повышения квалификации пошел в ученики к Ян Ваньлу, мулле из мечети города Тунчжоу (провинция Хэбэй), у которого и изучил 18 мусульманских локтей.</p>

