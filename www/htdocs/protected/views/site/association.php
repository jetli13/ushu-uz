		<p>Такие стили как «цзяомэнь таньтуй» и «чжацюань»   так же являлись типичными стилями мусульман.
			Цзяомэнь таньтуй- «Таньтуй» («удар ногой, подобный распрямлению лука») — это хлесткий удар ногой снизу вверх, чаще всего направленный в пах противника. Когда-то приемы «таньтуй» были частью стиля чжацюань.                 Их было 28, каждое из них соответствовало одной из букв арабского алфавита.
			Позже в качестве начальных приемов (упражнений) остались первые десять, а последние восемнадцать были скомпонованы в два комплекса «туйцюань» («кулак ног»). Десять же первых приемов, известные как «шилу таньтуй» («десять дорожек таньтуй») широко распространились в народе и часто практиковались отдельно, в качестве простой и быстро осваиваемой техники самообороны.  
			Чжацюань  О происхождении этого стиля существует много легенд, противоречащих одна другой, а также историческим данным. Самая достоверная описывает происхождение чжацюань так:
			Чжа Шанъи,  был родом из Синьцзяна (северо-запад Китая, примыкающий к Средней Азии), жил при династии Мин. Когда в приморских провинциях Китая развернулась борьба с японскими набегами, и был издан императорский указ о наборе добровольцев, Чжа  Шаньи вступил в отряд, идущий на восток. В долгом пути он заболел, и товарищи оставили его на попечение местных крестьян-мусульман в уезде Гуаньсянь провинции Шаньдун на северо-востоке Китая. После нескольких месяцев лечения он поправился. В благодарность за кров и уход Чжа  Шаньи обучил крестьян боевому искусству, которым владел. В честь его они назвали этот стиль «кулак Чжа» («чжацюань»).
			Чжацюань считается одним из классических стилей категории «чанцюань» («длинный кулак»), то есть, он предназначен для боя на дальней дистанции. 
			Танпинцюань -еще этот стиль называют «танпин циши» («7 форм сосуда для омовения»), «циши» («7 форм»).
			Очень древний стиль, известны каменные куклы времен династии Тан (618—907) изображающие дерущихся уйгуров, стоящих в позициях танпинцюаиь.. Стиль передавался только среди мусульман и только по мужской линии, поэтому его адептов никогда не было много.</p>

