			Период завоеваний Тамерлана ознаменовал собой наивысший расцвет военного дела среднеазиатских кочевников. Опираясь на богатый опыт своих предшественников, Тамерлан сумел создать мощную и боеспособную армию, позволившую ему одерживать блестящие победы на полях сражений над своими противниками. Эта армия была многонациональным и многоконфессиональным объединением, ядром которого являлись тюрко-монгольские воины-кочевники. Армия Тамерлана делилась на конницу и пехоту, роль которой сильно возросла на рубеже XIV—XV вв. Тем не менее, основную часть армии составляли конные отряды кочевников, костяк которых состоял из элитных подразделений тяжеловооружённых кавалеристов, а также отрядов телохранителей Тамерлана. Пехота зачастую играла вспомогательную роль, однако была необходима при осадах крепостей. Пехота была большей частью легковооружённой и в основном состояла из лучников, однако в армии состояли также тяжеловооружённые ударные отряды пехотинцев.
			Помимо основных родов войск (тяжёлой и лёгкой конницы, а также пехоты) в армии Тамерлана находились отряды понтонёров, рабочих, инженеров и прочих специалистов, а также особые пехотные части, специализировавшиеся на боевых операциях в горных условиях.
