		<p><b>
				Бацзицюань («кулак восьми пределов»).
			</b>
			Известны несколько легенд о происхождении стиля, но во всех их стиль берет начало от одного и того же человека — У Чжуна (1712—1802), мусульманина из деревни Хоучжуанкэ-цунь провинции Хэбэй. Помимо рукопашного боя, У Чжун также был знаменитым мастером боя на длинных копьях.
			У Чжун передал свое искусство родной дочери У Жун, приемному сыну У Юну и его родному брату У Чжунъюю. У Юн был родом из деревни Мэнцунь, и через него Мэнцунь стала «родиной бацзи», основным центром развития и распространения бацзицюань. Помимо Мэнцунь, важным центром бацзицюань стала соседняя деревня Лотун. К настоящему времени, помимо ортодоксального бацзицюань, известны также бацзи семьи  У (разработал У Сюфэн (1908—1976), семьи Цян (разработал Цян Жуйцин (1861 — 1947), семьи Ли (идет от Ли Шувэня (1864— 1934), семьи Хо (идет от Хо Дяньгэ (1886—1942) и семьи Ма (идет от Ма Фэту (1888—1973).
			Основную часть приемов составляют удары руками.
			Специальным видом перемещения бацзицюань является «трущий шаг», при ударе с продвижением вперед передняя нога должна двигаться по земле, преодолевая трение — таким образом, боец учится подавать вперед силу своего корпуса. Согласно легендам, мастера бацзицюань при ударе могли прорыть небольшую канавку, в связи с этим бытовало выражение «кулак словно чертит по земле».</p>

