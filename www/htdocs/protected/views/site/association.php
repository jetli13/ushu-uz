				<b>
					В третьих, В Средние века торговцы из  государств Ближнего Востока и Средней Азии приезжали в Китай по Великому шелковому пути.
				</b>
				Часть из них оседала на постоянное жительство. Приезжали и проповедники, распространявшие учение ислама, им удавалось обратить в свою веру некоторых жителей Срединной империи. При династиях Тан и Сун, то есть до XIII века, мусульманское население Китая постепенно увеличивалось за счет иммигрантов, приезжавших по суше и морю — астрономов, математиков, врачей. 
			В официальных документах династии Юань их называли «хуэйхуэй»! В 1275 г. в соответствии с указом императора из династии Юань «всюду вступать в общины жителей приграничья» пришедшие на восток мусульманские пехотинцы и артиллеристы из солдат были превращены в крестьян. Постепенно в Китае сформировалась особая национальная группа — хуэйцзу («мусульмане»).
			В течение семисот лет своего существования   хуэицзу были тесно связаны с войнами. Это способствовало широкой популярности боевых искусств среди китайских мусульман.
